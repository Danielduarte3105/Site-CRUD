<!-- JavaScript do Modal -->
<script>
    document.addEventListener('DOMContentLoaded', function () {
        const modal = document.getElementById("infoModal");
        const modalName = document.getElementById("modalName");
        const modalRole = document.getElementById("modalRole");
        const modalDescription = document.getElementById("modalDescription");
        const closeModal = document.querySelector(".close");

        // Adicionar evento de clique para cada item da lista
        document.querySelectorAll('.flex-item a').forEach(function (item) {
            item.addEventListener('click', function (e) {
                e.preventDefault(); // Prevenir o redirecionamento padrão

                // Obter dados do elemento pai (li)
                const parent = this.closest('.flex-item');
                const name = parent.getAttribute('data-name');
                const role = parent.getAttribute('data-role');
                const description = parent.getAttribute('data-description');

                // Preencher o modal com os dados
                modalName.textContent = name;
                modalRole.textContent = role;
                modalDescription.textContent = description;

                // Mostrar o modal
                modal.style.display = "block";
            });
        });

        // Fechar o modal
        closeModal.addEventListener('click', function () {
            modal.style.display = "none";
        });

        // Fechar o modal ao clicar fora dele
        window.addEventListener('click', function (e) {
            if (e.target == modal) {
                modal.style.display = "none";
            }
        });
    });
</script>



